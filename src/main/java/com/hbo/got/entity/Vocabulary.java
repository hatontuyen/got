package com.hbo.got.entity;


public class Vocabulary {
	private long id;
	private String voca;
	private String brPro;
	private String amPro;
	private String brVoice;
	private String amVoice;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getVoca() {
		return voca;
	}
	public void setVoca(String voca) {
		this.voca = voca;
	}
	public String getBrPro() {
		return brPro;
	}
	public void setBrPro(String brPro) {
		this.brPro = brPro;
	}
	public String getAmPro() {
		return amPro;
	}
	public void setAmPro(String amPro) {
		this.amPro = amPro;
	}
	public String getBrVoice() {
		return brVoice;
	}
	public void setBrVoice(String brVoice) {
		this.brVoice = brVoice;
	}
	public String getAmVoice() {
		return amVoice;
	}
	public void setAmVoice(String amVoice) {
		this.amVoice = amVoice;
	}
	
	
	
}

